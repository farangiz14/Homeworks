--18--
SELECT CUSTOMERID, COUNT(ORDERID) AS Order_Count
FROM ORDERS
GROUP BY CUSTOMERID
HAVING COUNT(ORDERID) >= 3; 

--19--
SELECT DEPARTMENTNAME, SUM(SALARY) AS Total_Salary
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME
HAVING SUM(SALARY) > 500000;


--20--

SELECT PRODUCTID, AVG(SALEAMOUNT) AS Avg_Sales
FROM SALES
GROUP BY PRODUCTID
