--23--
SELECT CUSTOMERID, MAX(TOTALAMOUNT) AS max_order_value, MIN(TOTALAMOUNT) AS min_order_value
FROM ORDERS
GROUP BY CUSTOMERID
HAVING MAX(TOTALAMOUNT) < 50;

--24--
