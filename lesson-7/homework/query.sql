--10--
SELECT DEPARTMENTNAME, COUNT(EMPLOYEEID) AS Employee_per_Dep_count
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME
HAVING COUNT(EMPLOYEEID) > 5;


--11--
SELECT PRODUCTID, SUM(SALEAMOUNT) AS Total_Sales, AVG(SALEAMOUNT) AS Average_Sales
FROM Sales
GROUP BY PRODUCTID;

--12--
SELECT DEPARTMENTNAME, COUNT(EMPLOYEEID)
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME
HAVING DEPARTMENTNAME = 'HR';

--13--

SELECT DEPARTMENTNAME, MIN(SALARY) AS lowest_salary, MAX(SALARY) AS highest_salary
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME;

--14--
SELECT DEPARTMENTNAME, AVG(SALARY) AS Avg_salary_by_Department
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME;

--15--
SELECT DEPARTMENTNAME, AVG(SALARY) AS Avg_salary_by_Department, COUNT(EMPLOYEEID) AS Employee_Count_per_Department
FROM EMPLOYEES
GROUP BY DEPARTMENTNAME;

--16--
SELECT CATEGORY, AVG(PRICE) AS Avg_Price
FROM Products
GROUP BY CATEGORY
HAVING AVG(PRICE) > 400;

--17--
SELECT YEAR(SALEDATE) AS Sales_Year, SUM(SALEAMOUNT) AS Total_Sale
FROM SALES
